// ─── Env Config ─────────────────────────────────────────────────────────────

function genEnvConfig(ir: AnvilIR): string {
  const envVars: string[] = [];

  if (ir.service.auth?.env_var) {
    envVars.push(`/** ${ir.service.auth.type} authentication */\nexport const ${ir.service.auth.env_var} = env('${ir.service.auth.env_var}');`);
  }

  if (ir.service.base_url) {
    envVars.push(`/** Service base URL */\nexport const BASE_URL = env('BASE_URL', '${ir.service.base_url}');`);
  }

  return `/**
 * Environment configuration for ${ir.service.name}
 * Generated by Anvil v${ir.meta.anvil_version}
 */

function env(key: string, fallback?: string): string {
  const value = process.env[key] ?? fallback;
  if (!value) throw new Error(\`Missing required env var: \${key}\`);
  return value;
}

${envVars.length > 0 ? envVars.join('\n\n') : '// No environment variables required'}
`;
}

